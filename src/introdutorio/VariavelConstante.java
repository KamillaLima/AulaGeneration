package introdutorio;

public class VariavelConstante {

	public static void main(String[] args) {
		float preco = 10.5f;
		//precisa colocar o f no final para que ele saiba que se trata de 
		//um numero float e não double
		
		final int TAXA = 4;
		//o final é pra simbolizar que se trata de uma constante	
		//deve ser com letra maiuscula;

		//double já é um default,por isso não necessita colocar a letra d no final
		//para exemplificar de qual se trata.
		//É válido não utilizar com valores ,tipo moeda.
		
	}

}
